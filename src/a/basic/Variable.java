package a.basic;

public class Variable {

    public static void main(String[] args) {
        int age;    // 선언
        age = 20;   // 할당
        System.out.printf("나이: %d", age);

        System.out.println();

        // 선언과 할당
        String firstName = "Kim";
        System.out.printf("성: %s", firstName);
    }
}
